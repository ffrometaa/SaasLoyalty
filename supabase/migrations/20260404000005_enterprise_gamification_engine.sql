-- ─── PG_CRON JOBS ───────────────────────────────────────────────────────────
-- Requires the pg_cron extension (enabled by default on Supabase Pro+).
-- These jobs call the run-scoring-engine edge function via pg_net.
-- Replace <PROJECT_REF> and <ANON_KEY> with actual Supabase project values,
-- or use vault secrets: select vault.decrypt_secret('scoring_anon_key')

-- Job 1: Score all members every 6 hours
SELECT cron.schedule(
  'enterprise-scoring-run',
  '0 */6 * * *',
  $$
  SELECT net.http_post(
    url := 'https://<PROJECT_REF>.supabase.co/functions/v1/run-scoring-engine',
    headers := '{"Content-Type": "application/json", "Authorization": "Bearer <ANON_KEY>"}'::jsonb,
    body := '{"trigger": "cron"}'::jsonb
  );
  $$
);

-- Job 2: Generate weekly leaderboard snapshots every Monday at 01:00 UTC
SELECT cron.schedule(
  'enterprise-leaderboard-weekly',
  '0 1 * * 1',
