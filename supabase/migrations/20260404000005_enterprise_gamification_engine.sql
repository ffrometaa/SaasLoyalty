-- ─── ENUMS ──────────────────────────────────────────────────────────────────
CREATE TYPE motivation_type AS ENUM ('achiever', 'socializer', 'explorer', 'competitor');
CREATE TYPE intervention_status AS ENUM ('pending', 'sent', 'dismissed', 'converted');
CREATE TYPE multiplier_condition AS ENUM (
  'always', 'tier_bronze', 'tier_silver', 'tier_gold', 'tier_platinum',
  'new_member', 'at_risk'
);

-- ─── 1. MEMBER BEHAVIOR SCORES ──────────────────────────────────────────────
-- One row per member; upserted by the scoring engine on each run.
CREATE TABLE member_behavior_scores (
  id               uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id        uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  member_id        uuid NOT NULL REFERENCES members(id) ON DELETE CASCADE,
  churn_score      numeric(4,3) NOT NULL DEFAULT 0
                   CHECK (churn_score BETWEEN 0 AND 1),
  engagement_score numeric(4,3) NOT NULL DEFAULT 0
                   CHECK (engagement_score BETWEEN 0 AND 1),
  motivation_type  motivation_type NOT NULL DEFAULT 'achiever',
  visit_velocity   numeric(6,2) NOT NULL DEFAULT 0,   -- avg visits per 30 days
  points_velocity  numeric(10,2) NOT NULL DEFAULT 0,  -- avg points earned per 30 days
  days_since_visit integer NOT NULL DEFAULT 0,
  scored_at        timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT unique_member_score UNIQUE (member_id)
);

-- ─── 2. MISSIONS ────────────────────────────────────────────────────────────
-- Multi-step challenge sequences authored by the tenant.
CREATE TABLE missions (
  id           uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id    uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  name         text NOT NULL,
  description  text,
  bonus_points integer NOT NULL DEFAULT 0 CHECK (bonus_points >= 0),
  badge_id     uuid REFERENCES badges(id) ON DELETE SET NULL,
  starts_at    timestamptz,
  ends_at      timestamptz,
  is_active    boolean NOT NULL DEFAULT true,
  created_at   timestamptz NOT NULL DEFAULT now(),
  updated_at   timestamptz NOT NULL DEFAULT now()
);

-- ─── 3. MISSION STEPS ───────────────────────────────────────────────────────
-- Ordered list of challenges that form a mission.
CREATE TABLE mission_steps (
  id           uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  mission_id   uuid NOT NULL REFERENCES missions(id) ON DELETE CASCADE,
  challenge_id uuid NOT NULL REFERENCES challenges(id) ON DELETE CASCADE,
  step_order   integer NOT NULL DEFAULT 1,
  CONSTRAINT unique_mission_step UNIQUE (mission_id, step_order)
);

-- ─── 4. MEMBER MISSION PROGRESS ─────────────────────────────────────────────
CREATE TABLE member_mission_progress (
  id              uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id       uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  member_id       uuid NOT NULL REFERENCES members(id) ON DELETE CASCADE,
  mission_id      uuid NOT NULL REFERENCES missions(id) ON DELETE CASCADE,
  steps_completed integer NOT NULL DEFAULT 0 CHECK (steps_completed >= 0),
  completed_at    timestamptz,
  bonus_awarded   boolean NOT NULL DEFAULT false,
  created_at      timestamptz NOT NULL DEFAULT now(),
  updated_at      timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT unique_member_mission UNIQUE (member_id, mission_id)
);

-- ─── 5. POINT MULTIPLIERS ───────────────────────────────────────────────────
-- Time-boxed multiplier events. Engine applies the highest active multiplier
-- that matches the member's condition on each visit.
CREATE TABLE point_multipliers (
  id          uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id   uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  name        text NOT NULL,
  multiplier  numeric(4,2) NOT NULL DEFAULT 2.0 CHECK (multiplier > 1),
  condition   multiplier_condition NOT NULL DEFAULT 'always',
  starts_at   timestamptz NOT NULL,
  ends_at     timestamptz NOT NULL,
  is_active   boolean NOT NULL DEFAULT true,
  created_at  timestamptz NOT NULL DEFAULT now(),
  CHECK (ends_at > starts_at)
);

-- ─── 6. LEADERBOARD SNAPSHOTS ───────────────────────────────────────────────
-- Periodic snapshots generated by the engine (monthly/weekly).
-- entries = [{rank, member_id, name, avatar_url, points, tier, delta}]
CREATE TABLE leaderboard_snapshots (
  id           uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id    uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  period_key   text NOT NULL,  -- e.g. '2026-04' or '2026-W14'
  period_type  text NOT NULL CHECK (period_type IN ('month', 'week')),
  entries      jsonb NOT NULL DEFAULT '[]',
  generated_at timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT unique_tenant_period UNIQUE (tenant_id, period_key, period_type)
);

-- ─── 7. ENGINE ACTIVITY LOG ─────────────────────────────────────────────────
-- Append-only audit log of every engine invocation and notable event.
CREATE TABLE engine_activity_log (
  id               uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id        uuid REFERENCES tenants(id) ON DELETE SET NULL,
  event_type       text NOT NULL,
  -- 'scoring_run' | 'intervention_triggered' | 'multiplier_applied'
  -- | 'mission_completed' | 'leaderboard_generated' | 'dynamic_challenge_created'
  payload          jsonb NOT NULL DEFAULT '{}',
  members_affected integer NOT NULL DEFAULT 0,
  duration_ms      integer,
  created_at       timestamptz NOT NULL DEFAULT now()
);

-- ─── 8. CHURN INTERVENTIONS ─────────────────────────────────────────────────
-- One record per triggered intervention; tracks resolution lifecycle.
CREATE TABLE churn_interventions (
  id                uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id         uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  member_id         uuid NOT NULL REFERENCES members(id) ON DELETE CASCADE,
  churn_score       numeric(4,3) NOT NULL,
  intervention_type text NOT NULL,
  -- 'bonus_offer' | 'personal_challenge' | 'tier_reminder' | 'win_back_campaign'
  status            intervention_status NOT NULL DEFAULT 'pending',
  triggered_at      timestamptz NOT NULL DEFAULT now(),
  resolved_at       timestamptz,
  metadata          jsonb NOT NULL DEFAULT '{}'
);

-- ─── 9. DYNAMIC CHALLENGES ──────────────────────────────────────────────────
-- Engine-generated personalized challenges per member.
-- Distinct from tenant-authored challenges in the challenges table.
CREATE TABLE dynamic_challenges (
  id            uuid DEFAULT gen_random_uuid() PRIMARY KEY,
  tenant_id     uuid NOT NULL REFERENCES tenants(id) ON DELETE CASCADE,
  member_id     uuid NOT NULL REFERENCES members(id) ON DELETE CASCADE,
  name          text NOT NULL,
  description   text,
  type          challenge_type NOT NULL,
  goal_value    integer NOT NULL CHECK (goal_value > 0),
  bonus_points  integer NOT NULL DEFAULT 0 CHECK (bonus_points >= 0),
  current_value integer NOT NULL DEFAULT 0,
  expires_at    timestamptz NOT NULL,
  completed_at  timestamptz,
  is_dismissed  boolean NOT NULL DEFAULT false,
  created_at    timestamptz NOT NULL DEFAULT now()
);

